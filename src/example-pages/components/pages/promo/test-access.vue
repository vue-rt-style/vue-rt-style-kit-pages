            function smoothScroll(start, target, activeSlider) {
                if (start < (target - 1) || start > (target + 1)) {
                    if (start < target) {
                        let interval = target - start > 500 ? 25 : 12;
                        let int = setInterval(function(){
                            if (start > (target - 15)) start += 1;
                            else if (start > (target - 50)) start += 5;
                            else if (start > (target - 100)) start += 10;
                            else if (start > (target - 200)) start += 20;
                            else if (start > (target - 350)) start += 35;
                            else if (start > (target - 500)) start += 50;
                            else if (start > (target - 750)) start += 75;
                            activeSlider.wrapper.scrollTo(start, 0);
                            if (start >= target) {
                                setTimeout(function(){
                                    activeSlider.mayScroll = true;
                                    clearInterval(int);
                                }, 5)
                            }
                        }, interval);
                    } else {
                        let interval = start - target > 500 ? 25 : 12;
                        let int = setInterval(function(){
                            if (start < (target + 15)) start -= 1;
                            else if (start < (target + 50)) start -= 5;
                            else if (start < (target + 100)) start -= 10;
                            else if (start < (target + 200)) start -= 20;
                            else if (start < (target + 350)) start -= 35;
                            else if (start < (target + 500)) start -= 50;
                            else if (start < (target + 750)) start -= 75;
                            activeSlider.wrapper.scrollTo(start, 0);
                            if (start <= target) {
                                setTimeout(function(){
                                    activeSlider.mayScroll = true;
                                    clearInterval(int);
                                }, 6)
                            }
                        }, interval);
                    }
                }
            }
            function initDesktopSliders() {
                slidersArray = [];
                document.querySelectorAll('.custom-slider').forEach(function(item){
                    var wrapper = item.querySelector('.custom-slider__inner');
                    slidersArray.push({
                        name: item.closest('.custom-slider').id,
                        wrapper: wrapper,
                        farRight: false,
                        farLeft: true,
                        mayScroll: true,
                        fullWidth: wrapper.scrollWidth - wrapper.offsetWidth
                    });
                    if(wrapper.scrollWidth > wrapper.offsetWidth) {

                        wrapper.addEventListener('scroll', function(){
                            var farLeft = wrapper.scrollLeft <= 10;
                            var farRight = wrapper.scrollLeft >= wrapper.scrollWidth - containerWidth - 10;
                            var activeSlider = slidersArray.find(el => el.name == item.id);
                            activeSlider.farRight = farRight;
                            activeSlider.farLeft = farLeft;
                            if(activeSlider.farLeft) {
                                item.querySelector('.custom-slider__controls-backing-left').classList.add('custom-slider__controls-backing--hidden');
                            } else {
                                item.querySelector('.custom-slider__controls-backing-left').classList.remove('custom-slider__controls-backing--hidden');
                            }
                            if(activeSlider.farRight) {
                                item.querySelector('.custom-slider__controls-backing-right').classList.add('custom-slider__controls-backing--hidden');
                            } else {
                                item.querySelector('.custom-slider__controls-backing-right').classList.remove('custom-slider__controls-backing--hidden');
                            }
                        });
                        item.querySelector('.custom-slider__controls-backing-right').addEventListener('click', function(){
                            var activeSlider = slidersArray.find(el => el.name == item.id);
                            countScroll(activeSlider, true);
                        });
                        item.querySelector('.custom-slider__controls-backing-left').addEventListener('click', function(){
                            var activeSlider = slidersArray.find(el => el.name == item.id);
                            countScroll(activeSlider, false);
                        });
                    }
                    item.querySelectorAll('.custom-slider__item').forEach(function(slide){
                        slide.querySelector('.game__popup__carousel-slide-backing').style.opacity = 1;
                        slide.querySelector('.game__popup__carousel-slide-border').style.opacity = 1;
                        slide.querySelector('.game__popup__carousel-slide').style.opacity = 1;
                    })
                });
            }
            function initMobileSliders() {
                document.querySelectorAll('.custom-slider').forEach(function(item){
                    var slideList = item.querySelectorAll('.custom-slider__item');
                    var slideArray = Array.from(slideList);
                    countSlidesOpacity(slideArray);
                    item.querySelector('.custom-slider__inner').addEventListener('scroll', function(){
                        countSlidesOpacity(slideArray);
                    })
                });
            }
            function countSlidesOpacity(slideArray) {
                slideArray.map(function(item){
                    if(item.getBoundingClientRect().left < 0 || item.getBoundingClientRect().right > window.innerWidth) {
                        item.querySelector('.game__popup__carousel-slide-backing').style.opacity = 0.5;
                        item.querySelector('.game__popup__carousel-slide-border').style.opacity = 0.5;
                        item.querySelector('.game__popup__carousel-slide').style.opacity = 0.5;
                    } else {
                        item.querySelector('.game__popup__carousel-slide-backing').style.opacity = 1;
                        item.querySelector('.game__popup__carousel-slide-border').style.opacity = 1;
                        item.querySelector('.game__popup__carousel-slide').style.opacity = 1;
                    }
                })
            }
            function initTabs() {
                if(window.innerWidth > 767) {
                    document.querySelectorAll('.popup-tabs__navigation-item').forEach(function(item){
                        item.addEventListener('click', function(){
                            var wrapper = item.closest('.popup-tabs');
                            var tabsNavigationList = wrapper.querySelectorAll('.popup-tabs__navigation-item');
                            var tabsNavigationArray = Array.from(tabsNavigationList);
                            var activeTabIndex = tabsNavigationArray.findIndex(function(el){return el == item});
                            wrapper.querySelector('.popup-tabs__navigation-item--is-active').classList.remove('popup-tabs__navigation-item--is-active');
                            tabsNavigationArray[activeTabIndex].classList.add('popup-tabs__navigation-item--is-active');
                            var tabsContentList = wrapper.querySelectorAll('.popup-tabs__content-item');
                            var tabsContentArray = Array.from(tabsContentList);
                            tabsContentArray.map(function(tab, index){
                                if(index == activeTabIndex){
                                    tab.classList.add('popup-tabs__content-item--is-active');
                                } else {
                                    tab.classList.remove('popup-tabs__content-item--is-active');
                                }
                            })
                        })
                    })
                } else {
                    document.querySelectorAll('.popup-tabs__navigation-arrows-right').forEach(function(item){
                        item.addEventListener('click', function(){
                            arrowNavigation(item, true)
                        })
                    });
                    document.querySelectorAll('.popup-tabs__navigation-arrows-left').forEach(function(item){
                        item.addEventListener('click', function(){
                            arrowNavigation(item, false)
                        })
                    })
                }
            }
            function arrowNavigation(item, direction) {
                var wrapper = item.closest('.popup-tabs');
                var tabsNavigationList = wrapper.querySelectorAll('.popup-tabs__navigation-item');
                var tabsNavigationArray = Array.from(tabsNavigationList);
                var activeTabIndex = tabsNavigationArray.findIndex(function(el){return el.classList.contains('popup-tabs__navigation-item--is-active')});
                var tabsContentList = wrapper.querySelectorAll('.popup-tabs__content-item');
                var tabsContentArray = Array.from(tabsContentList);
                if(direction && activeTabIndex < tabsNavigationArray.length - 1) {
                    wrapper.querySelector('.popup-tabs__navigation-item--is-active').classList.remove('popup-tabs__navigation-item--is-active');
                    tabsNavigationArray[activeTabIndex + 1].classList.add('popup-tabs__navigation-item--is-active');
                    wrapper.querySelector('.popup-tabs__content-item--is-active').classList.remove('popup-tabs__content-item--is-active');
                    tabsContentArray[activeTabIndex + 1].classList.add('popup-tabs__content-item--is-active');
                    wrapper.querySelector('.popup-tabs__navigation-arrows-left').classList.remove('popup-tabs__navigation-arrows--inactive');
                    if(activeTabIndex + 1 == tabsNavigationArray.length - 1) {
                        wrapper.querySelector('.popup-tabs__navigation-arrows-right').classList.add('popup-tabs__navigation-arrows--inactive');
                    }
                    initMobileSliders();
                }
                if(!direction && activeTabIndex > 0) {
                    wrapper.querySelector('.popup-tabs__navigation-item--is-active').classList.remove('popup-tabs__navigation-item--is-active');
                    tabsNavigationArray[activeTabIndex - 1].classList.add('popup-tabs__navigation-item--is-active');
                    wrapper.querySelector('.popup-tabs__content-item--is-active').classList.remove('popup-tabs__content-item--is-active');
                    tabsContentArray[activeTabIndex - 1].classList.add('popup-tabs__content-item--is-active');
                    wrapper.querySelector('.popup-tabs__navigation-arrows-right').classList.remove('popup-tabs__navigation-arrows--inactive');
                    if(activeTabIndex - 1 == 0) {
                        wrapper.querySelector('.popup-tabs__navigation-arrows-left').classList.add('popup-tabs__navigation-arrows--inactive');
                    }
                    initMobileSliders();
                }
            }
            function debounce(func){
                var timer;
                return function(event){
                    if(timer) clearTimeout(timer);
                    timer = setTimeout(func, 500, event);
                };
            }

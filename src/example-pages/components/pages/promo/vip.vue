          var slideArray = document.querySelectorAll('.cloud-g-slider-item')
          var slide = slideArray[0];
          var step = 300
          if (!isAnimated) {
            isAnimated = true
            if (item.classList.contains('cloud-g-slider-arrow-left')) {
              var marginLeft = parseInt(slide.style.marginLeft);
              if(!marginLeft){
                marginLeft = 0
              }
              marginLeft-=step
              index= index - 1

              slide.style.marginLeft = marginLeft+'px'
              setTimeout(function () {
                isAnimated = false
                setRigthArrow()
              }, 200)
            } else {
              var marginLeft = parseInt(slide.style.marginLeft);
              marginLeft+=step
              index = index + 1

              slide.style.marginLeft = marginLeft+'px'
              setTimeout(function () {
                isAnimated = false
                setRigthArrow()
              }, 200)
            }
          }
        })
